
public class Tutor {
	
	private String name;
	private String year;
	
	public Tutor(String name, String year) {
		this.name = name;
		this.year = year;
	}
	
	public String getName() {
		return name;
	}
	
	public String getYear() {
		return year;
	}
	
	public String toString() {
		return name;
	}

}
